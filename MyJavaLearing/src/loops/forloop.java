package loops;
public class forloop {
    /*
    for(start; end;step){
    .....
    }
    */
   public static void main(String[] args) {
    for(int i =1; i<=5; i++){
        System.out.println(i);
    }
    System.out.println();
    for(int j =5; j>=1;j--)
    {
        System.out.println(j);
    }
   }
}
